
public class GarageDoor {
	public void up() {
		System.out.println("차고 문 열기");
	}
	
	public void down() {
		System.out.println("차고 문 닫기");
	}
	
	public void stop() {
		System.out.println("차고 정지");
	}
	
	public void lightOn() {
		System.out.println("차고 불 켜기");
	}
	
	public void lightOff() {
		System.out.println("차고 불 끄기");
	}
}
